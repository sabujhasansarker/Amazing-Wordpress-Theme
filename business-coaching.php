        <p>I learned from my mistakes, and in doing so paved my way to a winning record in the business world. Much of this occurred on the internet; over many years I developed web design, development, marketing, and security companies, all of which were successful despite being part of a growing and extremely competitive market. Some of my ventures were sold to larger companies, while others I am proud to say are still making me money today.</p>
